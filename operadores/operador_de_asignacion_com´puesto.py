print('*** Operadores de asignación compúestos ***')

a,b = 10 , 15

print(f'Vaor de a: {a}')
print(f'Vaor de b: {b}')

#Operador compuesto suma +=
a+=b
print(f'Valor de a += b {a}')

#Operador compuesto resta -=
a = 10
a -= b
print(f'Valor de a -= b {a}')

#Operador compuesto multiplicación *=
a = 10
a *= b
print(f'Valor de a *= b {a}')

#Operador compuesto multiplicación /=
a = 10
a /= b
print(f'Valor de a /= b {a}')

